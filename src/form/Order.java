    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn sản phẩm trước!", "Warning", 2);
        } else if (jTextField2.getText().isEmpty() || jTextField2.getText().equals("0")) {
            JOptionPane.showMessageDialog(this, "Hãy nhập số lượng!", "Warning", 2);
        } else {
            try {
                model = (DefaultTableModel) jTable1.getModel();
                int cid = Integer.parseInt(jTextField1.getText().trim());
                int qty = Integer.parseInt(jTextField2.getText().trim());
                int proId = Integer.parseInt(model.getValueAt(rowIndex, 0).toString());
                String pName = jTextField3.getText().trim();
                price = Double.parseDouble(model.getValueAt(rowIndex, 2).toString());
                if (!dao.isProductExist(cid, proId)) {
                    Cart cart = new Cart();
                    cart.setId(cid);
                    cart.setPid(proId);
                    cart.setpName(pName);
                    cart.setQty(qty);
                    cart.setPrice(price);
                    cart.setTotal(price * (double) qty);
                    total += price * (double) qty;
                    jLabel1.setText(String.format("Tổng tiền (VNĐ): " + "%.2f", total));
                    if (dao.insertCart(cart)) {
                        JOptionPane.showMessageDialog(this, "Sản phẩm đã được thêm");
                        clear();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Sản phẩm này đã tồn tại", "Warning", 2);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "" + e, "Warning", 2);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

